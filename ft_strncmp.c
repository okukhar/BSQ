#include <string.h>

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int		i;
	size_t	j;

	i = 0;
	j = 0;
	while (*(s1 + i) != '\0' && *(s2 + i) != '\0' && j < n)
	{
		if (*(s1 + i) < *(s2 + i))
			return (-1);
		else if (*(s1 + i) > *(s2 + i))
			return (1);
		i++;
		j++;
	}
	if (*(s1 + i) != '\0' && *(s2 + i) == '\0')
		return (1);
	else if (*(s1 + i) != '\0' && *(s2 + i) == '\0')
		return (-1);
	return (0);
}
